#include "libft.h"

int			ft_checkret(int ret)
{
	ft_putnbrendl(ret);
	if (ret > 566)
		return (0);
	if (ret < 21)
		return (0);
	if (ret % 21 != 20)
		return (0);
	return (1);
}
