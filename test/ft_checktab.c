#include "libft.h"

int			ft_checktab(char **tab)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (tab[i])
	{
		while (tab[j])
		{
			if (tab[i][j] == '#')
				if (tab[i][j + 1] != '#' && tab[i][j + 1] != '\0')
					if (tab[i][j - 1] != '#' && tab[i][j - 1] != '\0')
						if (tab[i + 1][j] != '#' && tab[i + 1] != NULL)
							if (tab[i - 1][j] != '#' && tab[i - 1] != NULL)
								return (0);
			j++;
		}
		j = 0;
		i++;
	}
	return (1);
}
