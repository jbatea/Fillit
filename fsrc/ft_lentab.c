#include "fillit.h"

int			ft_lentab(t_tetrimino *tetri)
{
	size_t	nb;
	size_t	i;

	i = 0;
	while ((tetri + i)->a != '\0')
		++i;
	nb = ft_nextsqrt(i * 4);
	return (nb);
}
